// ProjectManagementFeatures.ts
interface ProjectManagementFeatures {
    // Existing features
    communicationOptions: string[]; // Options for communication (audio, video, text, etc.)
    phases: string[]; // Phases of the project (ideation, team creation, product brainstorming, etc.)
    dataAnalysis: boolean; // Indicates if data analysis feature is available
    
    // New features
    projectTemplates: string[]; // Pre-defined project templates
    taskDependencies: boolean; // Ability to set task dependencies
    advancedReporting: boolean; // Advanced reporting capabilities
    thirdPartyIntegration: string[]; // Integrated with third-party tools
    aiPoweredInsights: boolean; // AI-powered insights
    communityForums: boolean; // Community forums
    multilingualSupport: boolean; // Multilingual support
    virtualWorkshops: boolean; // Virtual workshops and training
    gamification: boolean; // Gamification features
    feedbackMechanism: boolean; // Feedback mechanism for continuous improvement
}


